def print_qr(qrcode):
    """Виводить матрицю у вигляді красивих квадратиків"""
    print("\n--- Візуалізація QR-коду ---")
    for row in qrcode:
        line = ""
        for pixel in row:
            # 1 - чорний квадрат (██), 0 - порожнє місце (два пробіли)
            # Використовую два символи, щоб квадратик був більш схожим на квадрат
            line += "██" if pixel == 1 else "  "
        print(line)
    print("----------------------------\n")

def decode_qr_v1(qrcode):
    size = 21
    bits = []
    up = True
    
    def is_function_pattern(r, c):
        if (r < 9 and c < 9) or (r < 9 and c > 12) or (r > 12 and c < 9): return True
        if r == 6 or c == 6: return True
        return False

    c = size - 1
    while c > 0:
        if c == 6: c -= 1
        rows = range(size - 1, -1, -1) if up else range(size)
        for r in rows:
            for col in [c, c - 1]:
                if not is_function_pattern(r, col):
                    bit = qrcode[r][col]
                    # Маска 0: (рядок + колонка) % 2 == 0
                    if (r + col) % 2 == 0: bit = 1 - bit
                    bits.append(bit)
        up = not up
        c -= 2

    def bits_to_int(bit_list):
        res = 0
        for b in bit_list: res = (res << 1) | b
        return res

    try:
        length = bits_to_int(bits[4:12])
        message = ""
        for i in range(length):
            start = 12 + (i * 8)
            char_bits = bits[start : start + 8]
            message += chr(bits_to_int(char_bits))
        return message
    except:
        return "Помилка при розшифровці!"

